/**
 * Build a keyword string from selected categories
 * @param categories - Array of category keys
 * @param limit - Maximum number of keywords (default 20)
 */
export function buildKeywords(
  categories: ('core' | 'payment' | 'longTail' | 'differentiators')[],
  limit = 20
): string {
  const allKeywords: string[] = [];
  
  for (const category of categories) {
    switch (category) {
      case 'core':
        allKeywords.push(...CORE_KEYWORDS);
        break;
      case 'payment':
        allKeywords.push(...PAYMENT_KEYWORDS);
        break;
      case 'longTail':
        allKeywords.push(...LONG_TAIL_KEYWORDS);
        break;
      case 'differentiators':
        allKeywords.push(...DIFFERENTIATOR_KEYWORDS);
        break;
    }
  }
  
  return allKeywords.slice(0, limit).join(", ");
}

