/**
 * Build dynamic keywords for a product page
 */
export function buildProductKeywords(
  productName: string,
  category?: string | null,
  vendorName?: string | null,
  city = 'Abidjan'
): string {
  const keywords: string[] = [productName];
  
  if (category) {
    keywords.push(`${category} ${city}`);
    keywords.push(...getProductCategoryKeywords(category).slice(0, 3));
  }
  
  if (vendorName) {
    keywords.push(vendorName);
  }
  
  keywords.push(
    `artisanat ivoirien`,
    `idée cadeau ${category?.toLowerCase() || 'original'}`,
    `livraison Côte d'Ivoire`,
    `achat Mobile Money`
  );
  
  return keywords.slice(0, 15).join(", ");
}

/**
 * Build dynamic keywords for a fund/cagnotte page
 */
export function buildFundKeywords(
  occasion?: string | null,
  beneficiaryName?: string | null
): string {
  const keywords: string[] = [];
  
  if (occasion) {
    keywords.push(`cagnotte ${occasion}`);
    keywords.push(`pot commun ${occasion}`);
    keywords.push(...getOccasionKeywords(occasion).slice(0, 3));
  } else {
    keywords.push("cagnotte collective");
    keywords.push("pot commun en ligne");
  }
  
  keywords.push(
    "cotisation cadeau",
    "financer ensemble",
    `collecte argent ${occasion || 'cadeau'}`,
    "offrir à plusieurs Afrique",
    "contribution en ligne"
  );
  
  if (beneficiaryName) {
    keywords.push(`cadeau pour ${beneficiaryName}`);
  }
  
  return keywords.slice(0, 12).join(", ");
}

